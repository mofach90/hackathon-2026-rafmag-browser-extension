"""
Analyze fetched rafmag transcripts to extract show-grammar patterns.

Usage:
  python analyze.py

Reads every .json in transcripts/, sends them in one call to a strong Gemini
model, and writes the resulting Markdown to grammar.md.
"""

import json
import os
import sys
import argparse
from pathlib import Path

from dotenv import load_dotenv
from google import genai
from google.genai import errors
from google.genai import types

ROOT = Path(__file__).parent
TRANSCRIPTS_DIR = ROOT / "transcripts"
GRAMMAR_FILE = ROOT / "grammar.md"

DEFAULT_MODEL = "gemini-3.1-pro-preview"
DEFAULT_MAX_OUTPUT_TOKENS = 22000


MANUAL_ANNOTATION = """\
Manual calibration example from one episode.

These timestamps are approximate and may vary by a few seconds. Treat this as
ground truth for what the project owner means by "show content" vs "noisy
radio break". Use it to calibrate your analysis before generalizing patterns
across the other transcripts.

Video ID: yaeWOrjiDRM

Show content starts:
- 14:29 = 869s

Noisy radio break ranges:
- 27:00 to 32:19 = 1620s to 1939s
- 43:43 to 51:14 = 2623s to 3074s
- 01:03:35 to 01:23:20 = 3815s to 5000s
- 01:33:21 to 01:36:20 = 5601s to 5780s
- 01:50:34 to 01:51:50 = 6634s to 6710s
- 01:59:56 to 02:14:45 = 7196s to 8085s
- 02:26:30 to 02:28:13 = 8790s to 8893s

Show content resumes after each break at the end timestamp of that break.
The show ends at the end of the video.
"""


ANALYSIS_PROMPT = """\
You are analyzing transcripts from {n} episodes of "rafmag", a Tunisian radio
show broadcast on Diwan FM. The transcripts were auto-generated by YouTube,
include timestamps in seconds, and are noisy on dialectal Arabic (Tunisian
Derja, often code-switched with French). Treat noisy spellings charitably.

Your job: identify, with maximum specificity, the LINGUISTIC and STRUCTURAL
patterns that mark ad/promo breaks in this show, so we can build an
automated detector.

You are also given one manually annotated episode. Use that annotation as a
calibration example:
- In video yaeWOrjiDRM, the listed break ranges ARE real noisy radio breaks.
- Text immediately before each break start is the best place to look for
  break-entry phrases.
- Text immediately after each break end is the best place to look for
  break-return phrases.
- Do not treat the pre-show section before 869s as normal show content.
- Do not overfit to that single episode; use it to learn the boundary meaning,
  then check whether the same patterns appear in the other episodes.

Each episode appears below as:

==== EPISODE <videoId> ====
[seconds] text...
[seconds] text...

Extract and report the following sections, in this exact order:

## 1. Break-entry phrases
Distinct phrases the moderator uses RIGHT BEFORE going to a promo/ad block.
For each phrase:
- The phrase in original Tunisian Derja (Arabic script if present in the
  transcripts; transliteration is fine if the transcript uses Latin letters).
- Approximate English translation.
- How many of the {n} episodes contained at least one occurrence.
- 2–3 verbatim examples, each with the episode videoId and timestamp.

## 2. Break-return phrases
Same structure, for what the moderator says when returning from a break.

## 3. Columnist / chroniqueur patterns
Some rafmag episodes feature a guest contributor who delivers their own
segment (e.g. a recap of the previous show). For each distinct columnist
or recurring sub-segment you can identify:
- How it's introduced by the main moderator (phrase + 2–3 examples with
  videoId + timestamp).
- How it's closed (phrase + examples).
- Approximate position in the episode (start? middle? end?).
- Typical duration in minutes.
- Any phrases used inside or around the columnist segment that LOOK like
  break markers but are actually part of normal show flow.

## 4. Structural regularities
- Typical number of ad breaks per episode (give the distribution across the
  {n} episodes).
- Typical position of breaks (e.g. "first break ~10 min in", "last break
  ~15 min before end").
- Typical break duration in seconds (estimate from long gaps between
  main-show speech in the transcript, or from "we'll be right back" → "we're
  back" pairings).
- Predictable show skeleton if any (intro → columnist → break → segment X →
  break → segment Y → outro?).

## 5. False-positive traps
Patterns that LOOK like break markers but ARE NOT real breaks. For each:
- Why a naive matcher would flag it.
- Why it isn't a break.
- 1–2 verbatim examples with videoId + timestamp.

## 6. Confidence and caveats
- Which episodes had usable transcripts vs heavily noisy ones (by videoId).
- Which sections of your analysis are most uncertain, and why.

Output as a single Markdown document. No preamble, no closing remarks.
"""


def format_transcript(data: dict) -> str | None:
    if "error" in data:
        return None
    video_id = data["videoId"]
    lines = [f"==== EPISODE {video_id} ===="]
    for s in data["snippets"]:
        lines.append(f"[{int(s['start'])}] {s['text']}")
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Analyze rafmag transcripts and write grammar.md."
    )
    parser.add_argument(
        "--model",
        default=os.environ.get("GEMINI_MODEL", DEFAULT_MODEL),
        help=f"Gemini model to use. Defaults to GEMINI_MODEL or {DEFAULT_MODEL}.",
    )
    parser.add_argument(
        "--max-output-tokens",
        type=int,
        default=DEFAULT_MAX_OUTPUT_TOKENS,
        help=f"Output token budget. Defaults to {DEFAULT_MAX_OUTPUT_TOKENS}.",
    )
    return parser.parse_args()


def main() -> int:
    load_dotenv()
    args = parse_args()
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        print(
            "GEMINI_API_KEY is not set. Copy .env.example to .env and paste your key.",
            file=sys.stderr,
        )
        return 1

    if not TRANSCRIPTS_DIR.exists() or not any(TRANSCRIPTS_DIR.iterdir()):
        print(
            f"No transcripts found in {TRANSCRIPTS_DIR}. Run fetch_transcripts.py first.",
            file=sys.stderr,
        )
        return 1

    blocks: list[str] = []
    for path in sorted(TRANSCRIPTS_DIR.glob("*.json")):
        data = json.loads(path.read_text())
        formatted = format_transcript(data)
        if formatted is None:
            print(
                f"  skipping {path.name}: {data.get('error', 'unknown error')}",
                file=sys.stderr,
            )
            continue
        blocks.append(formatted)

    if not blocks:
        print("No usable transcripts to analyze.", file=sys.stderr)
        return 1

    n = len(blocks)
    prompt = ANALYSIS_PROMPT.format(n=n)
    full_input = prompt + "\n\n" + MANUAL_ANNOTATION + "\n\n" + "\n\n".join(blocks)

    print(f"Sending {n} transcripts to {args.model}...", file=sys.stderr)
    print(f"  input size: {len(full_input):,} characters", file=sys.stderr)
    print(f"  max output tokens: {args.max_output_tokens:,}", file=sys.stderr)

    client = genai.Client(api_key=api_key)

    try:
        response = client.models.generate_content(
            model=args.model,
            contents=full_input,
            config=types.GenerateContentConfig(
                # Markdown output, enough room for a detailed grammar without
                # pushing preview-model quota harder than necessary.
                max_output_tokens=args.max_output_tokens,
            ),
        )
    except errors.APIError as e:
        print(f"\nGemini API error: {e}", file=sys.stderr)
        if getattr(e, "code", None) == 429 or "RESOURCE_EXHAUSTED" in str(e):
            print(
                "\nQuota was exhausted. Try one of these:\n"
                "  GEMINI_MODEL=gemini-2.5-flash python analyze.py\n"
                "  python analyze.py --model gemini-2.5-flash --max-output-tokens 8000\n"
                "  python analyze.py --model gemini-2.5-pro --max-output-tokens 8000\n",
                file=sys.stderr,
            )
        return 1

    grammar = response.text or ""
    GRAMMAR_FILE.write_text(grammar)

    if response.usage_metadata is not None:
        print(
            f"\n[tokens] prompt={response.usage_metadata.prompt_token_count} "
            f"output={response.usage_metadata.candidates_token_count} "
            f"total={response.usage_metadata.total_token_count}",
            file=sys.stderr,
        )

    print(f"\nGrammar written to: {GRAMMAR_FILE}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
